def format_none(phrase):
    """
    Checks the given object against None and return "n/A" in case of.
    If the value not equals None, its return the object.

    | **param** phrase (str)
    | **return** not available (str)
    """
    if phrase == None:
        return "n/A"
    else:
        return phrase
